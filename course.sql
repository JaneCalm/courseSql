drop table if exists users;
create table users(
	id SERIAL PRIMARY KEY,
	firstname VARCHAR(100),
	lastname VARCHAR(100),
	email VARCHAR(120) not null unique,
	password_u VARCHAR(100) not null,
	gender CHAR(2),
